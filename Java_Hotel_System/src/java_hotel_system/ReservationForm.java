    public void removeRow() {
        DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
        int n = m.getRowCount();

        for (int i = n - 1; i >= 0; i--) {

            m.removeRow(i);

        }
    }

    public void show_data() {
        try {
            String sql = "select * from reservation_data";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
            while (rs.next()) {

                m.addRow(new Object[]{rs.getInt("Client_id"), rs.getString("Name"), rs.getString("Room_No"), rs.getString("Cost")});

            }
        } catch (SQLException ex) {
            Logger.getLogger(ClientsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void clear() {
        cid.setText("");
        cname.setText("");

        roomNo.setText("");
        total.setText("");
    }

