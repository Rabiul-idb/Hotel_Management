    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Reserved button
        String clid = cid.getText();
        String clname = cname.getText();
        String clroom = roomNo.getText();
        String clcost = total.getText();
        if (conn != null) {
            try {
                String sql = "insert into reservation_data (Client_id, Name, Room_No, Cost) values(?,?,?,?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, clid);
                pst.setString(2, clname);
                pst.setString(3, clroom);
                pst.setString(4, clcost);

                pst.execute();
                JOptionPane.showConfirmDialog(null, "Room Reservation successfull");
                removeRow();
                show_data();
                clear();
                String sqlu = "update room_data set Reserved ='yes' where Room_No='" + clroom + "'";
                pst = conn.prepareStatement(sqlu);
                pst.execute(sqlu);
            } catch (SQLException ex) {
                Logger.getLogger(ReservationForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    public void search_contant() {
        try {
            conn = DBConnection.getConnection();
            String sql = "select * from client_data where id=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, searchfield.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                cid.setText(rs.getString("id"));
                String fname = rs.getString("FirstName");
                String lname = rs.getString("lastName");

                cname.setText((fname + " " + lname));

            } else {
                JOptionPane.showMessageDialog(null, "Not Found data");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReservationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
