    public void Room_No() {
        try {
            conn = DBConnection.getConnection();
            String sql = "select * from room_data where Room_No=? and Reserved = ?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, searchfield1.getText());
            pst.setString(2, "No");
            rs = pst.executeQuery();
            if (rs.next()) {

                roomNo.setText(rs.getString("Room_No"));

            } else {
                JOptionPane.showMessageDialog(null, "This Room is Already Reserved");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReservationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

